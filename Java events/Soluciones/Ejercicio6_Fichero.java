    //EDAD PIERDE EL FOCO
    private void jFormattedTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextField1FocusLost
        if (!this.jFormattedTextField1.getText().equals("")) {
            try {
                int edad = Integer.parseInt(this.jFormattedTextField1.getText());
                if (edad < 4) {
                    JOptionPane.showMessageDialog(this, "La edad no puede ser inferior a 4 años", "Error", JOptionPane.ERROR_MESSAGE);
                    this.jFormattedTextField1.requestFocusInWindow(); //pide el foco
                    this.jFormattedTextField1.selectAll(); //se selecciona su contenido
                    this.jLabel4.setText(""); //limpio por si había algo de antes               
                } else if (!this.jLabel4.getText().equals("")) {
                    //comprobamos que la categoría es de la edad actual
                    if (!this.jLabel4.getText().equals(categoria(edad))) {
                        //si no coinciden, borro la categoria, porque es anterior
                        this.jLabel4.setText("");
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "La edad debe ser numérica", "Error", JOptionPane.ERROR_MESSAGE);
                this.jFormattedTextField1.requestFocusInWindow(); //pide el foco
                this.jFormattedTextField1.selectAll(); //se selecciona su contenido

            }
        }
    }//GEN-LAST:event_jFormattedTextField1FocusLost

    private String categoria(int edad) {
        try {
            if (edad >= 4 && edad < 10) {
                return "Infantil";
            } else if (edad >= 10 && edad < 16) {
                return "Alevín";
            } else if (edad >= 16 && edad < 20) {
                return "Juvenil";
            } else if (edad >= 20) {
                return "Amateur";
            } else {
                return "";
            }
        } catch (NumberFormatException e) {
            return "";
        }

    }
    
    //F1 EN LA EDAD
    private void jFormattedTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextField1KeyPressed
        int codTecla = evt.getKeyCode();
        if (codTecla == KeyEvent.VK_F1) {
            int edad = Integer.parseInt(this.jFormattedTextField1.getText());
            if (edad >= 4) { //Solo si la edad es correcta
                this.jLabel4.setText(categoria(edad));
            }
        } else {
            this.jLabel4.setText(""); //limpio por si había algo antes
        }
    }//GEN-LAST:event_jFormattedTextField1KeyPressed

    //BOTÓN ACEPTAR
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nom = this.jTextField1.getText();
            String sEdad = this.jFormattedTextField1.getText();
            int edad = sEdad.equals("") ? 0 : Integer.parseInt(sEdad);
            String direc = this.jTextField3.getText();
            Participante p = new Participante(nom, edad, direc);
            grabarParticipante(p);
            JOptionPane.showMessageDialog(this, "Participante almacenado", "Información", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
            this.jTextField1.requestFocus();
        } catch (Exception e) {
            int err = Integer.parseInt(e.getMessage());
            if (err == 1) {
                JOptionPane.showMessageDialog(this, "Error. Participante sin nombre", "Error", JOptionPane.ERROR_MESSAGE);
                this.jTextField1.requestFocus();
            } else if (err == 2) {
                JOptionPane.showMessageDialog(this, "Error. Participante sin edad", "Error", JOptionPane.ERROR_MESSAGE);
                this.jFormattedTextField1.requestFocusInWindow();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

