    private void abrirConexion() {
        try {
            fich = new File(ruta);
            fos = new FileOutputStream(fich,true); //Abrimos el fichero en modo APPEND
            oos = new ObjectOutputStream(fos);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Ejercicio6_Fichero.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Ejercicio6_Fichero.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cerrarConexion() {
        try {
            if (fos != null) {
                fos.close();
            }
            if (oos != null) {
                oos.close();
            }
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }

